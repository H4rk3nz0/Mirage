    /// Extract the extension-type sequence (in wire order) from a generated
    /// ClientHello record, skipping GREASE (0x?A?A). Used to assert the F4
    /// per-connection shuffle.
    fn extension_types(bytes: &[u8]) -> Vec<u16> {
        // record(5) + hs_header(4) + version(2) + random(32) + sid_len(1)+sid +
        // then cipher list, compression, extensions. Reuse the parser's shape by
        // walking from a known offset.
        let mut p = 5 + 4 + 2 + 32; // to session_id length
        let sid_len = bytes[p] as usize;
        p += 1 + sid_len;
        let cs_len = u16::from_be_bytes([bytes[p], bytes[p + 1]]) as usize;
        p += 2 + cs_len;
        let comp_len = bytes[p] as usize;
        p += 1 + comp_len;
        let ext_total = u16::from_be_bytes([bytes[p], bytes[p + 1]]) as usize;
        p += 2;
        let end = p + ext_total;
        let mut out = Vec::new();
        while p + 4 <= end {
            let t = u16::from_be_bytes([bytes[p], bytes[p + 1]]);
            let l = u16::from_be_bytes([bytes[p + 2], bytes[p + 3]]) as usize;
            if t & 0x0F0F != 0x0A0A {
                out.push(t);
            }
            p += 4 + l;
        }
        out
    }

    #[test]
    fn production_client_hello_has_structurally_valid_ech_grease() {
        // red-team HIGH #1: a production ClientHello MUST carry a GREASE ECH
        // (0xfe0d) whose framing is range-checkable, or a censor pins
        // "ML-KEM group but no ECH".
        let rnd = [0u8; 32];
        let sid = [0u8; 32];
        let ks = [0x33u8; 32];
        let ch = build_client_hello(&ClientHelloInputs {
            random: &rnd,
            session_id: &sid,
            x25519_key_share: &ks,
            server_name: "example.com",
            include_alpn: true,
            fingerprint: &CHROME_DESKTOP,
            grease: Some(GreaseValues::random()),
            mlkem_ek: None,
        })
        .unwrap();
        // Walk extensions to the ECH body.
        let mut p = 5 + 4 + 2 + 32;
        let sid_len = ch[p] as usize;
        p += 1 + sid_len;
        let cs_len = u16::from_be_bytes([ch[p], ch[p + 1]]) as usize;
        p += 2 + cs_len;
        let comp_len = ch[p] as usize;
        p += 1 + comp_len;
        let ext_total = u16::from_be_bytes([ch[p], ch[p + 1]]) as usize;
        p += 2;
        let end = p + ext_total;
        let mut ech: Option<&[u8]> = None;
        while p + 4 <= end {
            let t = u16::from_be_bytes([ch[p], ch[p + 1]]);
            let l = u16::from_be_bytes([ch[p + 2], ch[p + 3]]) as usize;
            if t == EXT_ENCRYPTED_CLIENT_HELLO {
                ech = Some(&ch[p + 4..p + 4 + l]);
            }
            p += 4 + l;
        }
        let body = ech.expect("production CH must carry a GREASE ECH extension");
        assert_eq!(body[0], 0x00, "ECHClientHelloType = outer");
        assert_eq!(&body[1..3], &[0x00, 0x01], "HKDF-SHA256");
        assert_eq!(&body[3..5], &[0x00, 0x01], "AES-128-GCM");
        let enc_len = u16::from_be_bytes([body[6], body[7]]) as usize;
        assert_eq!(enc_len, 32, "X25519 enc is 32 bytes");
        let payload_len = u16::from_be_bytes([body[8 + 32], body[8 + 32 + 1]]) as usize;
        assert_eq!(payload_len, ECH_GREASE_PAYLOAD_LEN);
        assert_eq!(body.len(), 1 + 4 + 1 + 2 + 32 + 2 + ECH_GREASE_PAYLOAD_LEN);
    }

    #[test]
    fn extension_order_shuffles_per_connection_but_set_is_stable() {
        let rnd = [0u8; 32];
        let sid = [0u8; 32];
        let ks = [0x33u8; 32];
        let mk = || {
            build_client_hello(&ClientHelloInputs {
                random: &rnd,
                session_id: &sid,
                x25519_key_share: &ks,
                server_name: "example.com",
                include_alpn: true,
                fingerprint: &CHROME_DESKTOP,
                grease: Some(GreaseValues::random()),
                mlkem_ek: None,
            })
            .unwrap()
        };
        // The canonical (non-GREASE) extension set, sorted - JA4-style.
        let mut canonical: Vec<u16> = CHROME_DESKTOP.extension_order.to_vec();
        canonical.sort_unstable();

        let mut saw_distinct_order = false;
        let first = extension_types(&mk());
        for _ in 0..24 {
            let types = extension_types(&mk());
            // Same SET every time (sorted equality) - JA4 stable.
            let mut sorted = types.clone();
            sorted.sort_unstable();
            assert_eq!(sorted, canonical, "extension set must be stable");
            if types != first {
                saw_distinct_order = true;
            }
            // Every generated CH must still parse.
            assert!(parse_client_hello_record(&mk()).is_ok());
        }
        assert!(
            saw_distinct_order,
            "F4: extension order must vary across connections"
        );
    }

    #[test]
    fn deterministic_when_no_grease() {
        // grease=None (tests) -> canonical order, byte-identical across calls.
        let rnd = [0u8; 32];
        let sid = [0u8; 32];
        let ks = [0x33u8; 32];
        let mk = || build_client_hello(&sample_inputs(&rnd, &sid, &ks)).unwrap();
        assert_eq!(mk(), mk(), "no-GREASE build must be deterministic");
        // And the emitted order equals the template's canonical order, minus the
        // production-only GREASE ECH (random payload, omitted in the no-GREASE
        // deterministic path so the build stays byte-reproducible).
        let expected: Vec<u16> = CHROME_DESKTOP
            .extension_order
            .iter()
            .copied()
            .filter(|&e| e != EXT_ENCRYPTED_CLIENT_HELLO)
            .collect();
        assert_eq!(extension_types(&mk()), expected);
    }

